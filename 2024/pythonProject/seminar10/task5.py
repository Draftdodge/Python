class Animal:
    def __init__(self, name: str):
        self.name = name


class Bird(Animal):
    def __init__(self, name: str, wingspan: float | int):
        super().__init__(name)
        self.wingspan = wingspan

    def wing_length(self):
        return self.wingspan / 2


class Fish(Animal):
    def __init__(self, name: str, max_depth: float | int):
        super().__init__(name)
        self.max_depth = max_depth

    def depth(self):
        if self.max_depth < 10:
            return '–ú–µ–ª–∫–æ–≤–æ–¥–Ω–∞—è —Ä—ã–±–∞'
        elif self.max_depth > 100:
            return '–ì–ª—É–±–æ–∫–æ–≤–æ–¥–Ω–∞—è —Ä—ã–±–∞'
        return '–°—Ä–µ–¥–Ω–µ–≤–æ–¥–Ω–∞—è —Ä—ã–±–∞'


class Mammal(Animal):
    def __init__(self, name: str, weight: float | int):
        super().__init__(name)
        self.weight = weight

    def category(self):
        if self.weight < 1:
            return '–ú–∞–ª—è–≤–∫–∞'
        elif self.weight > 200:
            return '–ì–∏–≥–∞–Ω—Ç'
        return '–û–±—ã—á–Ω—ã–π'


