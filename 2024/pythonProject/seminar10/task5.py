    def create_animal(animal_type=str, *args):
        if animal_type == 'Bird':
            anim = Bird(*args)
        elif animal_type == 'Fish':
            anim = Fish(*args)
        elif animal_type == 'Mammal':
            anim = Mammal(*args)
